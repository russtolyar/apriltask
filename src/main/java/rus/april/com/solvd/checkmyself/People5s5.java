package rus.april.com.solvd.checkmyself;

public class People5s5 {
    private String type;
    private String name;
    private int money;

    public People5s5(String type, String name, int money) {
        this.type = type;
        this.name = name;
        this.money = money;
    }

    public String GetType() {
        return this.type;
    }

    public String GetName() {
        return this.name;
    }

    public int GetMoney() {
        return this.money;
    }

}